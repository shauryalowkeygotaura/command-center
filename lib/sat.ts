// SAT GRIND data layer - a localStorage FSRS-4.5 spaced-repetition deck driving
// toward the SAT 1550 target (Aug 2026). Pure client-side, static-export safe:
// nothing here touches a network or a backend, mirroring lib/storage.ts.
//
// Why FSRS over textbook SM-2 (the old scheduler): SM-2 resets the interval to 1
// day on any lapse (wasting reviews) and drifts about 16 percent from a retention
// target. FSRS-4.5 with fixed default weights is deterministic, needs no training
// or backend, hits a 90 percent target within about 5 percent, and is what Anki
// 23.10 ships. The whole scheduler is the pure function gradeCard() below.
//
// Three learning levers are baked in here, each grounded in the cited research:
//   - Spaced repetition (Dunlosky 2013 HIGH-utility): FSRS schedules each card as
//     its recall probability decays.
//   - The spacing effect (Cepeda 2006): requestRetention tapers UP under 30 days
//     and every interval is clamped to the test date, so the countdown shapes the
//     schedule instead of being cosmetic.
//   - Interleaving (Rohrer and Taylor 2007): dueCards() emits subjects round-robin
//     (and rotates math by skill) so the queue mixes instead of blocking.
//
// Two consumers read this module:
//   - components/SatGrind.tsx renders the drill and grades cards.
//   - the now.json feed (scripts/build_now.py) reports the `studying` half.
//     The tab persists a derived snapshot (nowStudyingBlock) to localStorage
//     and can download it as data/now-studying.json for the build script to
//     pick up - the only bridge between client state and the static feed.

import { isoDate } from "./day";

export const SAT_TARGET = 1550;
export const SAT_TEST_DATE = "2026-08"; // College Board August 2026 SAT

export type Subject = "math" | "reading" | "writing" | "vocab";

export interface Card {
  id: string;
  front: string;
  back: string;
  subject: Subject;
  /** FSRS memory stability in days (expected interval at requestRetention). */
  stability: number;
  /** FSRS difficulty, clamped 1..10. */
  difficulty: number;
  /** current inter-repetition interval in days (derived from stability). */
  interval: number;
  /** YYYY-MM-DD the card is next due. */
  due: string;
  /** ISO timestamp of the last grade, or null if never reviewed. */
  lastReviewed: string | null;
  /** finer math discrimination so the math bucket can rotate by skill type. */
  skill?: string;
  /** optional elaboration shown on reveal (elaborative interrogation). */
  why?: string;
  /** last few confidence-vs-outcome pairs (metacognitive calibration), capped. */
  calibration?: { predicted: "sure" | "unsure"; correct: boolean }[];
  /** generated by the starter seed, not hand-added. */
  seeded?: boolean;
}

// Review log so streak / reviewed-today survive reloads without re-deriving
// from card timestamps (which lose per-day counts once a card is re-due).
export interface SatProgress {
  /** YYYY-MM-DD -> number of grades that day. */
  reviewsByDay: Record<string, number>;
  /** YYYY-MM-DD -> first-attempt retrieval success vs total attempts. */
  recallByDay: Record<string, { correct: number; attempted: number }>;
  /** ISO timestamp of the most recent grade across all days. */
  lastSessionAt: string | null;
  /** free-text focus the user is grinding (drives now.json current_focus). */
  focus: string;
}

export interface SatState {
  cards: Card[];
  progress: SatProgress;
}

